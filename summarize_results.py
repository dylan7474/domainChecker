import csv

ORIGINAL_FILE = 'domains.csv'
INPUT_FILE = 'results.csv'
OUTPUT_REPORT = 'live_domains_summary.csv'

def summarize_results():
    # Dictionaries to hold unique domains for each category
    categories = {
        "Tier 1: Verified Application (Definitively Live)": set(),
        "Tier 1b: Browser Verified (DNS/IP Mismatch or WAF)": set(),
        "Tier 2: SSL/TLS Protected (Definitively Live)": set(),
        "Tier 3: Silent/Firewalled (Network Live)": set(),
        "Tier 4: Inactive / Unreachable": set(),
        "Other": set()
    }

    all_original_domains = set()
    active_domains = set()

    def normalize_domain(value):
        cleaned = value.strip().lower().rstrip('.')
        return cleaned.lstrip('*. -').replace('*', '')

    # 1. Read the original domains list to know the baseline
    print(f"Reading {ORIGINAL_FILE} to determine baseline...")
    try:
        with open(ORIGINAL_FILE, 'r', encoding='utf-8') as f:
            reader = csv.DictReader(f)
            for row in reader:
                domain = normalize_domain((row.get('Domain') or row.get('Hostname') or ''))
                if domain:
                    all_original_domains.add(domain)
    except FileNotFoundError:
        print(f"Warning: {ORIGINAL_FILE} not found. Cannot determine inactive domains.\n")

    # 2. Read the results from the scan
    print(f"Reading {INPUT_FILE}...\n")
    try:
        with open(INPUT_FILE, 'r', encoding='utf-8') as f:
            # We use DictReader to easily access columns by header name
            reader = csv.DictReader(f)
            
            for row in reader:
                hostname = row.get('Hostname', '').strip()
                status = row.get('Status', '').strip()
                hostname = normalize_domain(hostname)
                
                if not hostname:
                    continue
                
                # Keep track of any domain that responded in any way
                active_domains.add(hostname)

                # Categorize based on the exact status strings generated by the v2 scanner
                if "Verified" in status and "OPEN" in status:
                    categories["Tier 1: Verified Application (Definitively Live)"].add(hostname)
                elif "IP Mismatch/WAF" in status:
                    # Catch the new browser sanity check results
                    categories["Tier 1b: Browser Verified (DNS/IP Mismatch or WAF)"].add(hostname)
                elif "SSL-Error" in status:
                    categories["Tier 2: SSL/TLS Protected (Definitively Live)"].add(hostname)
                elif "Silent" in status:
                    categories["Tier 3: Silent/Firewalled (Network Live)"].add(hostname)
                else:
                    categories["Other"].add(hostname)
        
        # 3. Calculate the completely inactive domains
        if all_original_domains:
            inactive = all_original_domains - active_domains
            categories["Tier 4: Inactive / Unreachable"] = inactive

        # 4. Write the report to a CSV file
        print(f"Writing results to {OUTPUT_REPORT}...")
        with open(OUTPUT_REPORT, 'w', encoding='utf-8', newline='') as out_f:
            writer = csv.writer(out_f)
            
            # Write the header row
            writer.writerow(['Domain', 'Category'])

            for category, domains in categories.items():
                if not domains:
                    continue
                
                # Sort domains alphabetically for readability
                sorted_domains = sorted(list(domains))
                
                for domain in sorted_domains:
                    writer.writerow([domain, category])

        print(f"\nSuccess! Full summary CSV saved to: {OUTPUT_REPORT}")

    except FileNotFoundError:
        print(f"Error: Could not find {INPUT_FILE}. Make sure the scanner has finished running.")
    except Exception as e:
        print(f"An error occurred: {e}")

if __name__ == "__main__":
    summarize_results()
